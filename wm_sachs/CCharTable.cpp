CCharTable::CharPos CCharTable::m_Table[0x10000];

CCharTable::CharPos CCharTable::GetCharPos(unsigned __int32 chr)
{
    CharPos result;

    if (chr < 0x60)
    {
        if (CFontPatch::RenderState->FontStyle != 0)
        {
            chr = CFontPatch::FindSubFontCharacter(chr, CFontPatch::RenderState->FontStyle);

            if (chr == 0xD0)
            {
                chr = 0;
            }
        }

        result.rowIndex    = (chr >> 4);
        result.columnIndex = (chr & 0xF);
    }
    else
    {
        result = m_Table[chr + 0x20];
    }

    return result;
}

